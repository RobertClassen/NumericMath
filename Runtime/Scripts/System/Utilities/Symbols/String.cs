namespace WellDefinedTypes
{
	using System;
	using System.Collections;
	using System.Collections.Generic;

	public static class String
	{
		#region Fields
		/// <summary>
		/// Null
		/// </summary>
		public const string Null = null;
		/// <summary>
		/// Can be used for default parameters instead of <see cref ="System.String.Empty"/>.
		/// </summary>
		public const string Empty = "";

		public const string Backspace = "\b";
		public const string Tab = "\t";
		public const string LineFeed = "\n";
		/// <summary>
		/// Same as <see cref ="LineFeed"/>
		/// </summary>
		public const string Newline = LineFeed;

		public const string CarriageReturn = "\r";

		public const string Space = " ";
		public const string ExclamationMark = "!";
		public const string QuotationMark = "\"";
		/// <summary>
		/// Same as <see cref ="QuotationMark"/>
		/// </summary>
		public const string DoubleQuote = QuotationMark;
		public const string Hashtag = "#";
		public const string Dollar = "$";
		public const string Percent = "%";
		public const string Ampersand = "&";
		public const string Apostrophe = "\'";
		/// <summary>
		/// Same as <see cref ="Apostrophe"/>
		/// </summary>
		public const string SingleQuote = Apostrophe;
		public const string OpeningParenthesis = "(";
		public const string ClosingParenthesis = ")";
		public const string Asterisk = "*";
		public const string Plus = "+";
		public const string Comma = ",";
		public const string Hyphen = "-";
		/// <summary>
		/// Same as <see cref ="Hyphen"/>
		/// </summary>
		public const string Minus = Hyphen;
		public const string FullStop = ".";
		/// <summary>
		/// Same as <see cref ="FullStop"/>
		/// </summary>
		public const string Dot = FullStop;
		public const string Slash = "/";

		public const string Zero = "0";
		public const string One = "1";
		public const string Two = "2";
		public const string Three = "3";
		public const string Four = "4";
		public const string Five = "5";
		public const string Six = "6";
		public const string Seven = "7";
		public const string Eight = "8";
		public const string Nine = "9";

		public const string Colon = ":";
		public const string Semicolon = ":";
		public const string LessThan = "<";
		public const string Equal = "=";
		public const string GreaterThan = ">";
		public const string QuestionMark = "?";
		public const string AtSign = "@";

		// A - Z

		public const string OpeningBracket = "[";
		public const string Backslash = "\\";
		public const string ClosingBracket = "]";
		public const string Caret = "^";
		public const string Underscore = "_";
		public const string GraveAccent = "`";
		/// <summary>
		/// Same as <see cref ="GraveAccent"/>
		/// </summary>
		public const string Backquote = GraveAccent;

		// a - z

		public const string OpeningBrace = "{";
		public const string Pipe = "|";
		public const string ClosingBrace = "}";
		public const string Tilde = "~";
		#endregion

		#region Properties
		
		#endregion

		#region Constructors
		
		#endregion

		#region Methods
		
		#endregion
	}
}