namespace WellDefinedTypes
{
	using System;
	using System.Collections;
	using System.Collections.Generic;

	public static class Char
	{
		#region Constants
		/// <summary>
		/// <c>'\0'</c>
		/// </summary>
		public const char Null = System.Char.MinValue;
		/// <summary>
		/// Same as <see cref ="Null"/>
		/// </summary>
		public const char Empty = Null;

		public const char Backspace = '\b';
		public const char Tab = '\t';
		public const char LineFeed = '\n';
		/// <summary>
		/// Same as <see cref ="LineFeed"/>
		/// </summary>
		public const char Newline = LineFeed;

		public const char CarriageReturn = '\r';

		public const char Space = ' ';
		public const char ExclamationMark = '!';
		public const char QuotationMark = '"';
		/// <summary>
		/// Same as <see cref ="QuotationMark"/>
		/// </summary>
		public const char DoubleQuote = QuotationMark;
		public const char Hashtag = '#';
		public const char Dollar = '$';
		public const char Percent = '%';
		public const char Ampersand = '&';
		public const char Apostrophe = '\'';
		/// <summary>
		/// Same as <see cref ="Apostrophe"/>
		/// </summary>
		public const char SingleQuote = Apostrophe;
		public const char OpeningParenthesis = '(';
		public const char ClosingParenthesis = ')';
		public const char Asterisk = '*';
		public const char Plus = '+';
		public const char Comma = ',';
		public const char Hyphen = '-';
		/// <summary>
		/// Same as <see cref ="Hyphen"/>
		/// </summary>
		public const char Minus = Hyphen;
		public const char FullStop = '.';
		/// <summary>
		/// Same as <see cref ="FullStop"/>
		/// </summary>
		public const char Dot = FullStop;
		public const char Slash = '/';

		public const char Zero = '0';
		public const char One = '1';
		public const char Two = '2';
		public const char Three = '3';
		public const char Four = '4';
		public const char Five = '5';
		public const char Six = '6';
		public const char Seven = '7';
		public const char Eight = '8';
		public const char Nine = '9';

		public const char Colon = ':';
		public const char Semicolon = ':';
		public const char LessThan = '<';
		public const char Equal = '=';
		public const char GreaterThan = '>';
		public const char QuestionMark = '?';
		public const char AtSign = '@';

		// A - Z

		public const char OpeningBracket = '[';
		public const char Backslash = '\\';
		public const char ClosingBracket = ']';
		public const char Caret = '^';
		public const char Underscore = '_';
		public const char GraveAccent = '`';
		/// <summary>
		/// Same as <see cref ="GraveAccent"/>
		/// </summary>
		public const char Backquote = GraveAccent;

		// a - z

		public const char OpeningBrace = '{';
		public const char Pipe = '|';
		public const char ClosingBrace = '}';
		public const char Tilde = '~';
		#endregion

		#region Fields
		
		#endregion

		#region Properties
		
		#endregion

		#region Constructors
		
		#endregion

		#region Methods
		
		#endregion
	}
}